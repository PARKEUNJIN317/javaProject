package variable;

public class VariableExample {
	
	public static void main(String[] args) {
		//변수선언
			int value ;
		//변수에 값을 저장
			value = 10;
		// 변수 선언 및 값 저장
			int result = value + 10 ;
		//변수에 저장된 값 출력
			System.out.println(result);
		//변수 저장 값 변경
			value = 50;
			System.out.println(value);
	}
}

